CREATE TABLE IF NOT EXISTS exercise 
(
    exerciseId 			SERIAL PRIMARY KEY,
    exerciseName 		VARCHAR(200) NOT NULL,
    equipmentUsed 		VARCHAR(100) NOT NULL,
    targetedBodypart 	VARCHAR(100) NOT NULL,
    entryUsername 		VARCHAR(100) NOT NULL,
    
    CHECK (equipmentUsed IN ('BARBELL', 'DUMBBELL', 'KETTLEBELL', 'EZBAR', 'SMITH MACHINE', 'MACHINE', 'ASSISTED MACHINE', 'SINGLE PULLEY CABLE', 'DOUBLE PULLEY CABLE', 'BODYWEIGHT')),
    CHECK (targetedBodypart IN ('CHEST', 'SHOULDERS', 'ARMS', 'BACK', 'LEGS', 'ABS')),
    UNIQUE (exerciseName, equipmentUsed, targetedBodypart, entryUsername)
);

